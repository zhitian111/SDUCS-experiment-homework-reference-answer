CREATE OR REPLACE VIEW TEST2_06 AS
    SELECT
        DISTINCT PUB.STUDENT.SID,
        PUB.STUDENT.NAME,
        PUB.STUDENT_COURSE.SCORE
    FROM
        PUB.STUDENT,
        PUB.STUDENT_COURSE,
        PUB.COURSE
    WHERE
        PUB.STUDENT.CLASS = '2010'
        AND PUB.STUDENT.DNAME = '计算机科学与技术学院'
        AND PUB.COURSE.CID = PUB.STUDENT_COURSE.CID
        AND PUB.COURSE.NAME = '操作系统'
        AND PUB.STUDENT_COURSE.SID = PUB.STUDENT.SID;


