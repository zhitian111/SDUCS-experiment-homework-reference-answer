SELECT
    MESSAGE.CONTENT,
    USER.NICKNAME
FROM
    MESSAGE
    JOIN SEND
    ON SEND.MID = MESSAGE.MID
    JOIN USER
    ON SEND.UID_SENDER = USER.UID
WHERE
    MESSAGE.CONTENT LIKE "%晚安%"
    AND( (SEND.UID_SENDER = 1
    AND SEND.UID_RECEIVER = 6)
    OR (SEND.UID_SENDER = 6
    AND SEND.UID_RECEIVER = 1) )
    AND SEND.STATUS = "已发送";