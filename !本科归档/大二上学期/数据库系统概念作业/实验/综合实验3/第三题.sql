SELECT
    COMMODITY.NAME,
    ONSELL.PRICE,
    COUNT(*) AS CNT
FROM
    STORES,
    ONSELL
    JOIN ORDERS
    ON STORES.SID = ORDERS.SID
    AND ONSELL.CID = ORDERS.CID
    JOIN COMMODITY
    ON ONSELL.CID = COMMODITY.CID
WHERE
    STORES.SID = 1
    AND ONSELL.SID = STORES.SID
GROUP BY
    ONSELL.CID
ORDER BY
    COUNT(*) DESC LIMIT 10;