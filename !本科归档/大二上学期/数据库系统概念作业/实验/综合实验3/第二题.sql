SELECT
    STORES.NAME,
    SUM(ONSELL.PRICE) AS MONEY
FROM
    ORDERS,
    COMMODITY
    JOIN ONSELL
    ON ORDERS.SID = ONSELL.SID
    AND ONSELL.CID = ORDERS.CID
    JOIN BRAND
    ON COMMODITY.BID = BRAND.BID
    JOIN STORES
    ON ORDERS.SID = STORES.SID
WHERE
    COMMODITY.CID = ORDERS.CID
    AND BRAND.NAME = "蒂芙尼"
GROUP BY
    ORDERS.SID
ORDER BY
    SUM(ONSELL.PRICE) DESC LIMIT 5;