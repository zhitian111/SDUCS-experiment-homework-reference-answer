SELECT
    COMMODITY.NAME,
    SUM(ONSELL.PRICE)     AS SM,
    AVG(ORDERS.BUYERRATE) AS AV
FROM
    BRAND,
    ONSELL
    JOIN COMMODITY
    ON BRAND.BID = COMMODITY.BID
    AND COMMODITY.CID = ONSELL.CID
    JOIN ORDERS
    ON ORDERS.CID = COMMODITY.CID
WHERE
    BRAND.BID = 1
    AND ORDERS.STATUS = "已完成"
GROUP BY
    ORDERS.CID;